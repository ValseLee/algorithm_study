#include<bits/stdc++.h>
using namespace std;

int main() { // alphabets to ASCII Numbers
    char a = 'a';
    cout << (int)a << "\n"; // 97
    cout << (int)a - 97 << "\n"; // 0
    cout << (int)a - 'a' << "\n"; // 0

    char b = 'b';
    char A = 'A';

    cout << (int)A << "\n"; // 65
    cout << (int)b << "\n"; // 98
    return 0;
}